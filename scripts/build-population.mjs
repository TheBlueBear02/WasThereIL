import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const csvPath = join(root, 'data', 'israel population (by age).csv')
const outPath = join(root, 'src', 'data', 'cbs-population.ts')

const raw = readFileSync(csvPath, 'utf-8')
const lines = raw.trim().split(/\r?\n/).slice(1)

const buckets = lines.map((line) => {
  const [amountRaw, ageRaw] = line.split(',')
  const amountThousands = parseFloat(amountRaw.trim())
  const ageStr = ageRaw.trim()

  const age = ageStr.includes('+') ? 95 : parseInt(ageStr, 10)
  const total = Math.round(amountThousands * 1000)

  return { age, total }
})

buckets.sort((a, b) => a.age - b.age)

const totalPopulation = buckets.reduce((sum, b) => sum + b.total, 0)

const body = buckets
  .map((b) => `  { age: ${b.age}, total: ${b.total} },`)
  .join('\n')

const output = `// Auto-generated by scripts/build-population.mjs — do not edit manually
import type { AgeBucket } from '../types'

export const CBS_POPULATION: AgeBucket[] = [
${body}
]

export const TOTAL_POPULATION = ${totalPopulation}
`

mkdirSync(dirname(outPath), { recursive: true })
writeFileSync(outPath, output, 'utf-8')

console.log(`Wrote ${buckets.length} age buckets, total = ${totalPopulation.toLocaleString()}`)
